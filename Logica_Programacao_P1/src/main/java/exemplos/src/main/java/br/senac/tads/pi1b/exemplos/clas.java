package exemplos.src.main.java.br.senac.tads.pi1b.exemplos;

public class clas {

    private Integer idade;

    public clas(Integer idade) {
        this.idadesTodos = idade;
    }

    @Override
    public String toString() {
        return "clas{" +
                "idade=" + idadesTodos +
                '}';
    }

    public Integer getIdade() {
        return idadesTodos;
    }
}
